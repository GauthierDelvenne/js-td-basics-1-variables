// Calculatrice
/*
Affichez dans la console les opérations de base avec les nombres 6 et 3 :
- leur somme
- leur différence
- leur multiplication
- leur division
- leur modulo
Avant d'exécuter le code, devinez à chaque fois le résultat de l'opération
*/
const a = 6;
const b = 3;
// Somme
const somme = a+b;
// Résultats = 9
console.log(somme);
// Différence
const diff = a-b;
// Résultats = 3
console.log(diff);
// Multiplication
const mult = a*b;
// Résultats = 18
console.log(mult);
// Division
const div = a/b;
// Résultats = 2
console.log(div);
// Modulo on utilise le % (le modulo est le reste de la division)
const mod = a%b;
// Résultats = 0
console.log(mod);